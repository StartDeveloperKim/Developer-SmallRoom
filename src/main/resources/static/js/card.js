export function updateArticleList(article, tag) {
    let $newArticle = $('<div>').addClass('w-full sm:w-1/2 md:w-1/2 lg:w-1/4 mb-4 px-2').attr('id', 'article' + article.articleId)
        .append($('<div>').addClass('bg-white rounded-lg shadow-lg transition duration-300 transform hover:-translate-y-1 hover:shadow-xl')
            .append($('<div>').addClass('block rounded-lg bg-white shadow-[0_2px_15px_-3px_rgba(0,0,0,0.07),0_10px_20px_-2px_rgba(0,0,0,0.04)] dark:bg-neutral-700')
                .append($('<a>').attr('href', '/article/' + article.articleId).attr('target', '_blank')
                    .append($('<img>').addClass('rounded-t-lg object-fill h-48 w-96').attr('src', article.thumbnailUrl).attr('alt', ''))
                )
                .append($('<div>').addClass('p-6')
                    .append($('<h5>').addClass('mb-2 text-xl font-bold leading-normal text-neutral-800 dark:text-neutral-50 truncate').text(article.title))
                    .append($('<p>').addClass('mb-4 text-base text-neutral-600 dark:text-neutral-200 truncate').text(article.subTitle))
                    .append($('<a>').attr('href', 'https://github.com/' + article.memberGithubId).attr('target', '_blank')
                        .append($('<p>').addClass('text-black font-extralight')
                            .append($('<i>').addClass('fab fa-brands fa-github'))
                            .append($('<span>').text(article.memberGithubId))
                        )
                    )
                    .append($('<div>').addClass('flex mt-2')
                        .append($('<div>').addClass('flex-auto text-black font-light text-xs').text(article.createAt))
                        .append($('<div>').addClass('flex-auto text-black font-thin text-sm flex items-center justify-end')
                            .append($('<div>').addClass('flex')
                                .append($('<div>').addClass('flex-auto items-center justify-end')
                                    .append($('<i>').addClass('fas fa-regular fa-eye'))
                                    .append($('<span>').text(article.hit))
                                )
                                .append($('<div>').addClass('flex-auto')
                                    .append($('<i>').addClass('fas fa-thin fa-heart'))
                                    .append($('<span>').text(article.likeCount))
                                )
                                /*.append($('<div>').addClass('flex-auto')
                                    .append($('<i>').addClass('fas fa-regular fa-comment fa-sm'))
                                    .append($('<span>').text(article.commentCount))
                                )*/
                            )
                        )
                    )
                )
            )
        );
    
    tag.append($newArticle);
}